
export default function getSumOfHoods(initialNumber, expansion1989, expansion2019) {
    if (expansion1989 = typeof expansion1989 !== "undefined" ? 3 : undefined) {
        return expansion1989;
    }

    if (expansion2019 = typeof expansion2019 !== "undefined" ? 4 : undefined) {
        return expansion2019;
    }
    return initialNumber + expansion1989 + expansion2019;
}